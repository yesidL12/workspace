'''
Escribí un programa que solicite al usuario ingresar
tres números para luego mostrarle el promedio de los
tres.
'''
num1 = int(input("Ingresa un número: "))
num2 = int(input("Ingresa un segundo número: "))
num3 = int(input("Ingresa un tercer número: "))

print(f"El promedio es", (num1 + num2 + num3) / 3)